// Variable defining my age to start. It is a const because it will not be changing
const myAge = 32;

// Variable defining early years. It's a let because it will be changing
let earlyYears = 2;

// Multiplying earlyYears by 10.5
earlyYears *= 10.5;

// Subtracting myAge by 2. That will be the new variable laterYears
let laterYears = myAge - 2;

// Multiply the laterYears variable by 4 to calculate the number of dog years accounted for by your later years.
laterYears *= 4;

// Adding earlyYears and laterYears to make a new variable called myAgeInDogYears
const myAgeInDogYears = earlyYears * laterYears

const myName = 'Harper'

'Harper'.toLowerCase();

console.log(`My name is ${myName}.  I am ${myAgeInDogYears} years old in dog years`)



